public class Loops{
    public static void main(String[] args){
        int i = 10;
        System.out.println("i");
        while (i > 0){
            System.out.println(i + " ");
            i--;
        }
        
        int j = 10;
        System.out.println("j");
        do{
            System.out.println(j);
            j++;
        }while(j<21);
    }
}